
package belajarjava;


public class method_dengan_parameter {

   static double Bagi(double n, double a){
       return n/a;
   }
    
    public static void main(String[] args) {
        //contoh dengan parameter a dan n
        double n = 5;
        double a =10;
        System.out.println(Bagi(n,a));
       
    }
    
}
