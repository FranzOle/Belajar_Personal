# Operator Bitwise

# Operator AND bitwise (&)
a = 5  # Representasi biner: 0101
b = 3  # Representasi biner: 0011
result_and = a & b
print(result_and)  # Output: 1 (Representasi biner: 0001)

# Operator OR bitwise (|)
a = 5  # Representasi biner: 0101
b = 3  # Representasi biner: 0011
result_or = a | b
print(result_or)  # Output: 7 (Representasi biner: 0111)

# Operator XOR bitwise (^)
a = 5  # Representasi biner: 0101
b = 3  # Representasi biner: 0011
result_xor = a ^ b
print(result_xor)  # Output: 6 (Representasi biner: 0110)

# Operator NOT bitwise (~)
a = 5  # Representasi biner: 0101
result_not = ~a
print(result_not)  # Output: -6 (Representasi biner: 11111010)

# Operator Left Shift (<<)
a = 5  # Representasi biner: 0101
result_left_shift = a << 1
print(result_left_shift)  # Output: 10 (Representasi biner: 1010)

# Operator Right Shift (>>)
a = 5  # Representasi biner: 0101
result_right_shift = a >> 1
print(result_right_shift)  # Output: 2 (Representasi biner: 0010)
