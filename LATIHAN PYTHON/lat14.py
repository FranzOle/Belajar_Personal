# Operasi assignment
# Operator Aritmetika dengan Assignment
x = 10
x += 5  # Sama dengan x = x + 5
print(x)  # Output: 15

y = 20
y -= 8  # Sama dengan y = y - 8
print(y)  

z = 6
z *= 4  
print(z)  

w = 15
w /= 3 
print(w)  # Output: 5.0 

# Operator Modulus dengan Assignment
m = 17
m %= 5  
print(m)  

# Operator Floor Division dengan Assignment
n = 27
n //= 4  
print(n)  

# Operator Pangkat dengan
p = 2
p **= 3
print(p)  

# Operator AND bitwise dengan Assignment
q = 5   
r = 3   
q &= r 
print(q)  

# Operator OR bitwise dengan Assgnment
s = 8  # R
t = 2  
s |= t  
print(s)  

# Operator XOR bitwise dengan Assignment
u = 5  
v = 3  
u ^= v 
print(u)  
