# Operasi assignment
# Operator Aritmetika dengan Assignment
x = 10
x += 5  # Sama dengan x = x + 5
print(x)  # Output: 15

y = 20
y -= 8  # Sama dengan y = y - 8
print(y)  # Output: 12

z = 6
z *= 4  # Sama dengan z = z * 4
print(z)  # Output: 24

w = 15
w /= 3  # Sama dengan w = w / 3
print(w)  # Output: 5.0 (hasil pembagian selalu float)

# Operator Modulus dengan Assignment
m = 17
m %= 5  # Sama dengan m = m % 5
print(m)  # Output: 2

# Operator Floor Division dengan Assignment
n = 27
n //= 4  # Sama dengan n = n // 4
print(n)  # Output: 6

# Operator Pangkat dengan Assignment
p = 2
p **= 3  # Sama dengan p = p ** 3
print(p)  # Output: 8

# Operator AND bitwise dengan Assignment (&)
q = 5  # Representasi biner: 0101
r = 3  # Representasi biner: 0011
q &= r  # Sama dengan q = q & r
print(q)  # Output: 1 (Representasi biner: 0001)

# Operator OR bitwise dengan Assignment (|)
s = 8  # Representasi biner: 1000
t = 2  # Representasi biner: 0010
s |= t  # Sama dengan s = s | t
print(s)  # Output: 10 (Representasi biner: 1010)

# Operator XOR bitwise dengan Assignment (^)
u = 5  # Representasi biner: 0101
v = 3  # Representasi biner: 0011
u ^= v  # Sama dengan u = u ^ v
print(u)  # Output: 6 (Representasi biner: 0110)
